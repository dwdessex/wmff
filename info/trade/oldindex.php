<h2 class="subtitle">Trade in 2018</h2>
<p>We are now sending out invitations to most of the traders who were with us last year, they will be going out over the next few days. 
General applications follow shortly.<p>

<p>Applications for trading at the festival, which will be held over the weekend of 8, 9, 10 June 2018 are will open very soon. Stay up to date with festival information by joining us on <a href="http://facebook.com/WimborneFolk" rel="tag" target="_blank">Facebook</a>, <a href="http://twitter.com/WimborneFolk" rel="tag" target="_blank">Twitter</a> & <a href="http://instagram.com/WimborneFolk" rel="tag" target="_blank">Instagram</a>.</p>

<p>Prices for 2017 trade stands are listed below, prices for 2018 will be known soon. Any queries should be sent to <a href="mailto:trade@wimbornefolk.co.uk"><strong>trade@wimbornefolk.co.uk</strong></a>.</p>

<p>Trade Stands are not allocated on a first come first served basis, each application is assessed on the choice, quality, origin and pricing structure to ensure a fair and varied amount of traders are offered a place.</p>

<P>We're changing the way we do trade stands this year.  It's won't be first come, first served.  Instead, over the first week or two in November, we'll be contacting our favourite traders from last year to give them the first chance at a pitch. After that, we'll throw it open to everyone else.</p>

<p>You will need to pay a deposit to secure your place.</p>

<p>All applicants are required to provide their issurance, a completed Risk Assessment form. All Traders are required to provide evidence of Insurance and Environmental Health Certification, if applicable.</p>

