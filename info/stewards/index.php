<h2 class="maintitle">Stewards & Volunteers</h2>
<p>Volunteer at Wimborne Minster Folk Festival 2018.</p>

<p>We are looking for vibrant people to be part of the festival team who are prepared to work in shifts, for around 12 hours in total over the three day festival.</p>

<p>You must be over 18, reliable, have good people skills and be able to remain pleasant and cheerful whatever the circumstances!</p>

<p>In exchange for your time, we will give you a ticket for festival events concerts, comedy & entry to <a href="http://partyinthepaddock.com" rel="tag"><strong>Party In The Paddock</strong></a> plus a free souvenir programme, free parking and free camping!</p>

<ul>
<li>You will generally be working in teams.</li>
<li>Training will be given if necessary.</li>
<li>Certain jobs may require a DBS (Disclosure & Barring Service) check, which we can arrange.</li>
<li>You will need to sort out your own refreshments.</li>
<li>A free day/weekend pass will be given to stewards which covers festival concerts, ceilidhs, comedy and entry to <a href="http://partyinthepaddock.com" rel="tag"><strong>Party In The Paddock</strong></a>.</li>
<li>You will receive a free souvenir programme.</li>
<li>Free parking will be available for stewards at <a href="http://merleyhallfarm.co.uk/camping" rel="tag"><strong>Merley Hall Farm</strong></a>.</li>
</ul>

<p>Stewarding & Volunteer roles available:</p>

<ul>
