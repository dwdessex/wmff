<h2 class="maintitle">Getting Here</h2>
<p>Wimborne Minster Folk Festival takes place in the historic market town of Wimborne Minster in Dorset.</p>

<h2 class="subtitle">By Road</h2>

<p>Wimborne has good road connections with easy access from the A31, B3082 and B3078.</p>

<p>Enter your own directions to plan your journey by using the map in the right corner of this page.</p>

<p>There is parking available within the town's car parks, except for High Street Car Park during the festival. We would suggest you use the parking facilities at <a href="/info/camping" rel="bookmark"><strong>Meadows Camping</strong></a>, which offer all day parking for <strong>&#163;3.00</strong> and is within a 10 minute walk of the town centre <!--or <strong>QE School</strong> for <strong>&#163;5.00</strong> per day and is within a 7 minute walk of the town centre--> or <strong>Allenbourn School</strong> playing fields for <strong>&#163;5.00</strong> per day and is within a 5 minute walk of the town centre.</p>

<h2 class="subtitle">By Public Transport</h2>

<p>Our nearest train station is <strong>Poole</strong>, with train services operated by <a href="http://www.southwesttrains.co.uk" rel="tag" target="_blank">South West Trains</a>.

<p>Regular buses service Wimborne during the folk festival from Poole and Bournemouth. Bus services are operated by <a href="http://morebus.co.uk" rel="tag" target="_blank">More Buses</a> (3, 4 & 13).</p>

